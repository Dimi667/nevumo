def seed_review_translations():
    """Add all review translation keys to the database."""
    db = SessionLocal()
    try:
        for key, translations in REVIEW_TRANSLATIONS.items():
            # Ensure all supported languages have a value (fallback to English)
            complete_translations = {}
            for lang in SUPPORTED_LANGUAGES:
                complete_translations[lang] = translations.get(lang, translations.get('en', key))

            upsert_translation_values(db, key, complete_translations)
            print(f"✅ Seeded: {key}")

        print(f"\n🎉 Successfully seeded {len(REVIEW_TRANSLATIONS)} translation keys")
    except Exception as e:
        print(f"❌ Error seeding translations: {e}")
        db.rollback()
        raise
    finally:
        db.close()


if __name__ == "__main__":
    print("🌱 Seeding review system translations...")
    seed_review_translations()
    print("✨ Done!")
