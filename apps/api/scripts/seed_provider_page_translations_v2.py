def main():
    conn = psycopg2.connect(DATABASE_URL)
    cur = conn.cursor()
    for lang, namespace, key, value in TRANSLATIONS:
        combined_key = f"{namespace}.{key}"
        cur.execute("""
            INSERT INTO translations (lang, key, value)
            VALUES (%s, %s, %s)
            ON CONFLICT (lang, key) DO UPDATE SET value = EXCLUDED.value
        """, (lang, combined_key, value))
    conn.commit()
    cur.close()
    conn.close()
    print(f"Done: {len(TRANSLATIONS)} translation keys seeded.")

if __name__ == "__main__":
    main()
