NAMESPACE = "auth"


def main() -> None:
    db = SessionLocal()
    try:
        inserted = 0
        for lang, key, value in TRANSLATIONS:
            db.execute(
                text("""
                    INSERT INTO translations (lang, namespace, key, value)
                    VALUES (:lang, :namespace, :key, :value)
                    ON CONFLICT (lang, namespace, key)
                    DO UPDATE SET value = EXCLUDED.value
                """),
                {"lang": lang, "namespace": NAMESPACE, "key": key, "value": value},
            )
            inserted += 1
        db.commit()
        print(f"✅ Seeded {inserted} translation rows ({inserted // 34} keys × 34 languages)")
    except Exception as e:
        db.rollback()
        print(f"❌ Error: {e}")
        raise
    finally:
        db.close()


if __name__ == "__main__":
    main()